  	<h1 align="center" style="font-family: Edwardian Script ITC">  About Gaya College</h1>
    <p style="font-family:Cambria ">&nbsp;"Gaya College, Gaya  had a modest start in 1944 on the eastern side of the legendary Phalgu  River.  Immediately after Independence,  the College was shifted to the present campus in 1948. The chief intent of the  mission was to impart quality teaching in various disciplines of higher  education. It is blessed with a 57 acre sprawling campus. The college had  affiliations with different Universities of the State like Patna University,  Patna and Bihar University, Muzaffarpur and is proud of being the first  constituent unit of Magadh University, Bodh Gaya, established in 1962. <br>
      The College was accredited in 2008 with  NAAC Grade – A (CGPA : 3.08). The UGC granted CPE status to the College in  2010. The State Govt. of Bihar also selected this College as one of the  potentially excellent colleges of Bihar" </p>
    <p align="center"  style="font-family:Vivaldi "><strong>Curricular Aspects </strong></p>
     <p style="font-family:Cambria ">&nbsp;"The College upholds the vision of “Enlightenment and  personality development of younger generation through education to make them an  asset to the nation”.  The College has been striving since its inception with a mission to provide  quality education in modern disciplines and inculcating a sense of moral  values, national pride and universal brotherhood among students. <br>
      The College offers fifty (50) UG &amp;  PG programmes in the faculties of Humanities, Social Sciences, Science and  Commerce including vocational and professional courses of Management, Computer  Application, Information Technology and other inter-disciplinary courses. These  courses are offered with subsidized fee structure to serve low income students  coming from backward rural and urban background. <br>
    New teaching-learning techniques are  also adopted in different departments. Popular lectures are organized at the  departmental level. Guest lectures are arranged to enrich faculty and students  with innovations in higher education" </p>
    <p align="center"  style="font-family:Vivaldi "><strong>Teaching, Learning and Evaluation</strong></p>
    <p style="font-family:Cambria ">&nbsp; "Gaya College maintains a  student-centric environment conducive for quality education. Result of the  college touches 95-100% success in almost all subjects with highest percentage  in 1st Class. Remedial courses are conducted regularly for SC/ST/OBC  and Minority students. Departments conduct class room evaluation of students.  Extra classes are organized for sent up students to augment their study. <br>
      All the teachers of this college have  Ph.D. degree. They also supervise research scholars for award of Ph.D. degree.  Teachers are encouraged to attend workshops, national seminars and conference  and refresher courses. Thirty five (35) teachers participated in National  Science Congress. Thirty three (33) teaches presented papers. One teacher, each  of History and Pali department, got National award. <br>
      National and International seminars  were organized by the College. UGC sponsored seven national seminars were  organized besides special lectures by eminent scholars. Gaya College also  organized <strong>Third Bihar Science Conference</strong> on the college campus. Hist Excellency Dr. A P J Abdul Kalam, former President  of India addressed the conference as chief guest" </p>
    <p align="center"  style="font-family:Vivaldi"><strong>Research, Consultancy and Extension  </strong></p>
    <p style="font-family:Cambria "> &nbsp;"Research Promotion Cell of the College  disseminates information on research schemes of different national  institutions. College encourages faculty members to take up research projects  for which they are facilitated as per norms. During the last four years ten  (10) Minor Research Projects and one Major Research Project were taken up by  faculty members. <br>
      Out of 102 faculty members, 24 teachers  are actively engaged in supervising researchers for award of Ph.D. degree. <br>
      The College has also provided research  books in PG Seminar libraries. Science Labs have also been upgraded with  facility to researchers. Physics Lab is being developed as centre of  excellence. Central Library has also been enriched with new research and  reference books. <br>
     
      
